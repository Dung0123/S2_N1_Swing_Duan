    private TaiKhoan getForm() {
        TaiKhoan tk = new TaiKhoan();
        if (txtUserName.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "không để trống tài Khoản");
        }
        tk.setUserName(txtUserName.getText());
        
        if (txtPassWord.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "không để trống Mật Khẩu");
        }
        tk.setPassWord(txtPassWord.getText());
        
        if (cbbChucVu.getSelectedIndex() == 0) {
            tk.setRole("Admin");
        }
        if (cbbChucVu.getSelectedIndex() == 1) {
            tk.setRole("Manager");
        }
        if (cbbChucVu.getSelectedIndex() == 2) {
            tk.setRole("Employee");
        }
        
        return tk;
    }
    
    public void loadTable() {
        DefaultTableModel model = (DefaultTableModel) this.table.getModel();
        model.setRowCount(0);
        for (TaiKhoan tk : this.serv.selectAll()) {
            model.addRow(new Object[]{tk.getUserName(),
                tk.getPassWord(),
                tk.getRole()});
            
        }
        
    }
    
